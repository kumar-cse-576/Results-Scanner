    public String pathof(String src) {
        String dest;
        dest = "";
        int i = 0, j = 0;
        while (j < src.length()) {

            if (src.charAt(j) == '\\') {
                j++;
                dest += "\\\\";
                continue;
            }
            dest += src.charAt(j);
            j++;
        }

        return dest;
    }

