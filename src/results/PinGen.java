      public void writePins(){
       
        int i;
        try{
             FileWriter f=new FileWriter(pat);
        for(i=0;i<Pins.length;i++){
            if(Pins[i]==null)
                break;
        f.write(Pins[i]+"\n");
        }
        f.close();
        System.out.println("done");
        }catch(IOException e){
        System.out.println(e);
        }
        
      }
  
      
      public void makePins(){
     String ac=a.getText();
     String cc=b.getText();
     String bh=c.getText();
     String data;
     int range=Integer.parseInt(d.getText());
     int i=1;
     int j=0;
     Pins= new String[100];
     while(i<=range){
         if(i<10){
     data=ac+cc+"-"+bh+"-00"+String.valueOf(i);
         }else{
     data=ac+cc+"-"+bh+"-0"+String.valueOf(i);
         }
         //System.out.println(data);
      
        Pins[j]=data;
        j++;
        i++;
     }
     
    }
public String pathof(String src){
    String dest;
        dest = "";
    int i=0,j=0;
    while(j<src.length()){
    
    if(src.charAt(j)=='\\'){
    j++;
    dest+="\\\\";
    continue;
    }
    dest+=src.charAt(j);
    j++;
    }
    
    return dest;
    }

